    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            String nome = areaNome.getText();
            String cpf = areaCpf.getText();
            String telefone = areaTelefone.getText();
            String cargo = comboCargos.getSelectedItem().toString();
            if(nome.length() > 0 && cpf.length() > 0){
                Funcionario f = new Funcionario();
                f.setNome(nome);
                f.setCpf(cpf);
                f.setTelefone(telefone);
                f.setCargo(cargo);
                if(funcionariodao.create(f)){
                    JOptionPane.showMessageDialog(null, "Cadastrado.");
                    limparCampos();
                    atualizarTabela();
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int linha = tbl_funcionarios.getSelectedRow();
        if(linha != -1){
            int id = Integer.parseInt((String) tbl_funcionarios.getValueAt(linha, 0));
            try{
                if(funcionariodao.delete(id)){
                    JOptionPane.showMessageDialog(null,"Excluido");
                    atualizarTabela();
                }
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void habilitaCampos(){
        areaCpf.setEditable(true);
        areaNome.setEditable(true);
        areaTelefone.setEditable(true);
    }
    private void limparCampos(){
        areaCpf.setText("");
        areaNome.setText("");
        areaTelefone.setText("");
    }
