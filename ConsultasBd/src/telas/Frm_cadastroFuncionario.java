    private void preencheTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tbl_funcionarios.getModel();
        try {
            ArrayList<Funcionario> funcionarios = funcionariodao.read();
            for(Funcionario x : funcionarios){
                String[] dados = new String[4];
                dados[0] = x.getId()+""; 
                dados[1] = x.getNome(); 
                dados[2] = x.getCpf(); 
                dados[3] = x.getCargo(); 
                modelo.addRow(dados);
            }
        } catch (Exception ex) {
            Logger.getLogger(Frm_cadastroFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void atualizarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tbl_funcionarios.getModel();
        try {
            modelo.setNumRows(0);
            ArrayList<Funcionario> funcionarios = (ArrayList<Funcionario>) funcionariodao.read();
            for(Funcionario x : funcionarios){
                String[] dados = new String[4];
                dados[0] = x.getId()+""; 
                dados[1] = x.getNome(); 
                dados[2] = x.getCpf(); 
                dados[3] = x.getCargo(); 
                modelo.addRow(dados);
            }
        } catch (Exception ex) {
            Logger.getLogger(Frm_cadastroFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

